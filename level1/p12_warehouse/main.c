#include<stdio.h>
#include<windows.h>
#include<string.h>
#include<conio.h>
#define M 50
/*实现如下的菜单（按数字选择菜单功能）：
显示存货列表
入库
出库
退出程序
实现菜单对应功能（需记录货物的型号、数量等信息）；
程序启动时从文件中读取当前库存数据，退出时保存库存数据；
*/
typedef struct
{
	int num;			//货品编号
	char name[20];		//货品名称
	int stock;			//原始库存
	int in;				//入库数目
	int out;			//出库数目
	int amount;			//最终库存
	int warning_value;
	int state;			//库存状态
}goods;
goods s[M];				//用于存放货品信息
goods r[M];				//用于存放入库货品信息
goods t[M];				//用于存放出库货品信息

void Re_file();
void Stock_in();
void Stock_out();
void Display();
void Printf_back();
void Modify();
void Statistics();
int Wr_file();

int N;
int P;

int main()
{
	int sele;
	Re_file();    //读取货品信息
	sele=1;
	while(sele)
	{
		system("cls");
		printf("\n\n");
		printf("*********************************************\n");
		printf("*                                           *\n");
		printf("*       1.入库              2.出库          *\n");
		printf("*                                           *\n");
		printf("*                                           *\n");
		printf("*       3.存货              4.退出          *\n");
		printf("*                                           *\n");
		printf("*********************************************\n");
		printf("请选择功能序号:");
		scanf("%d",&sele);
		switch(sele)
		{
			case 1:Stock_in();Display();break;
			case 2:Stock_out();Display();break;
			case 3:Statistics();break;
			case 4:exit(0);sele=0;break;
		}
		printf("\n\n按任意键继续...\n");
		getch();
	}
	Wr_file();
	return 0;
}

void Re_file()		//读入原始库存文件
{
	FILE*fp;
	N=0;
	fp=fopen("goods.txt","r");
	while(fscanf(fp,"%d%s%d%d%d%d%d",&s[N].num,&s[N].name,&s[N].stock,&s[N].in,&s[N].out,&s[N].amount,&s[N].warning_value)!=EOF)N++;
	fclose(fp);
	P=N;
}

void Stock_in()		//读入入库文件
{
	FILE*fp;
	int i,j;
	N=0;
	fp=fopen("stockin.txt","r");
	while(fscanf(fp,"%d%d",&r[N].num,&r[N].in)!=EOF)N++;
	fclose(fp);
	for(i=0;i<P;i++)
	{
		for(j=0;j<N;j++)
		{
			if(r[i].num==s[j].num)
				s[j].in=r[j].in;
		}
	}
	for(i=0;i<P;i++)
	s[i].amount=s[i].stock+s[i].in;
}
void Stock_out()	//读入出库文件
{
	FILE*fp;
	int i,j;
	N=0;
	fp=fopen("stockout.txt","r");
	while(fscanf(fp,"%d%d",&t[N].num,&t[N].out)!=EOF)N++;
	fclose(fp);
	for(i=0;i<P;i++)
	{
		for(j=0;j<N;j++)
		{
			if(t[i].num==s[j].num)
				s[j].out=t[j].out;
		}
	}
	for(i=0;i<P;i++)
	s[i].amount=s[i].stock+s[i].in-s[i].out;
}

void Display()		//显示库存情况
{
	int i,j;
	system("cls");
	//Estimate();
	printf("货品编号  货品名称  原始库存  入库数目  出库数目  最终库存  \n");
	for(i=0,j=1;i<P;i++,j++)
	{
		if(s[i].state==0)
		{
			HANDLE handle = GetStdHandle(STD_OUTPUT_HANDLE);
			SetConsoleTextAttribute(handle, FOREGROUND_INTENSITY | FOREGROUND_RED| FOREGROUND_GREEN | FOREGROUND_BLUE);
			printf("%-9d  %-10s%-10d%-10d%-10d%-10d%-10d\n",s[i].num,s[i].name,s[i].stock,s[i].in,s[i].out,s[i].amount,s[i].warning_value);
			if(j%10==0&&j<P)
			{
				HANDLE handle = GetStdHandle(STD_OUTPUT_HANDLE);
				SetConsoleTextAttribute(handle, FOREGROUND_INTENSITY | FOREGROUND_RED| FOREGROUND_GREEN | FOREGROUND_BLUE);
				printf("按任意键继续...");
				getch();
				puts("\n");
				printf("货品编号  货品名称  原始库存  入库数目  出库数目  最终库存  \n");
			}
		}
	}
}

void Printf_back()     //返回主菜单
{
	getch();
	main();
}

void Statistics()       //统计数目
{
	int k,i,j,m=0,n=0;
	system("cls");
	printf("\n\n");
	printf("*********************************************\n");
	printf("*                                           *\n");
	printf("*                                           *\n");
	printf("*                                           *\n");
	printf("*      1.库存总数         2.库存状态        *\n");
	printf("*                                           *\n");
	printf("*                                           *\n");
	printf("*                                           *\n");
	printf("*********************************************\n");
	printf("\n\n请输入查询选项:");
	scanf("%d",&k);
	if(k==1)
	{
		for(i=0;i<P;i++)
			m=m+s[i].amount;
		printf("库存总数为：%d",m);
	}

	Wr_file();
}

int Wr_file()       //写输出文件
{
	FILE*fp;
	int i;
	fp=fopen("amount.txt","w");
	for(i=0;i<P;i++)
		fprintf(fp,"%-9d  %-10s%-10d%-10d%-10d%-10d%-10d\n",s[i].num,s[i].name,s[i].stock,s[i].in,s[i].out,s[i].amount,s[i].warning_value);
	fclose(fp);
	return 1;
}
